package nasa.com;

public class PossitionData {
	
	
	private int above;
	private  int below;
	
	
	public PossitionData(int above,int below){
		this.above = above;
		this.below = below;		
	}
	
	public PossitionData(){
		this.above=0;
		this.below=0;
	}
	
	public int getAbove(){
		return above;
	}
	
	public int getBelow(){
				
		return below;
	}
	
	
	
	
	
	

}
